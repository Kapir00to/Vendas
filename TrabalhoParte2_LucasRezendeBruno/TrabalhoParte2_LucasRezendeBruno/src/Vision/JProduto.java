    public void visualizar_tabela(){
    	modelo.setRowCount(0);
	for (Produto p : lista_p)
			modelo.addRow( new Object[]{ p.getNome(), p.getNum(), p.getCategoria(), p.getPreco()});	
		}
		
	public void removeProduto() {
		if (jTable1.getSelectedRow()>=0)
			lista_p.remove(jTable1.getSelectedRow());
	}
	
	private void adicionaProduto() {
		Produto p = new Produto(
			Integer.parseInt(jTextField2.getText()),
			Float.parseFloat(jTextField4.getText()),
			jTextField1.getText(),	
			jTextField3.getText()	
			);
		lista_p.add(p);
	}
