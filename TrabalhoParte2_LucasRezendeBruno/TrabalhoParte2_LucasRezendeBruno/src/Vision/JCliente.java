    public void visualizar_tabela(){
    	try{
    		ArrayList<Cliente> lista_c = Arquivo.carregar_cliente(path);   	
    		DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    		modelo.setRowCount(0);
    			for (Cliente c : lista_c)
    				modelo.addRow( new Object[]{ c.getNome(), c.getEmail(), c.getTelefone(), c.getCpf()});	

    	}catch (IOException e){
    		System.out.println(e.getMessage());
    	}
    }
		
	public void removePessoa() {
		try{
    		ArrayList<Cliente> lista_c = Arquivo.carregar_cliente(path);
		if (jTable1.getSelectedRow()>=0)
			lista_c.remove(jTable1.getSelectedRow());
			Arquivo.escrever_cliente(lista_c, path);
		}catch (IOException e){
    		System.out.println(e.getMessage());
    	}
	}
	
	private void adicionaPessoa() {
		try{
    		ArrayList<Cliente> lista_c = Arquivo.carregar_cliente(path);
		Cliente p = new Cliente(
			jTextField1.getText(),
			jTextField2.getText(),
			Integer.parseInt(jTextField3.getText()),
			Long.parseLong(jTextField4.getText())
			);
		lista_c.add(p);
		Arquivo.escrever_cliente(lista_c, path);
		}catch (IOException e){
    		System.out.println(e.getMessage());
    	}
	}
